


function decrement(){
    var b =  parseInt(document.getElementById("grid-item-2").innerHTML);
    b = b - 1;
    document.getElementById("grid-item-2").innerHTML = b;

}

function increment(){
    var b =  parseInt(document.getElementById("grid-item-2").innerHTML);
    b = b + 1;
    document.getElementById("grid-item-2").innerHTML = b;

}